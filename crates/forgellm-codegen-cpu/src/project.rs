//! Generate a complete Cargo project from a computation graph.
//!
//! Creates a directory structure with:
//! - Cargo.toml (with tokenizers dependency)
//! - src/model.rs (kernels + forward function)
//! - src/main.rs (weight loader + tokenizer + generation loop)

use std::fs;
use std::path::Path;

use forgellm_frontend::ir::*;

use crate::emit;

/// Generate a complete Cargo project from a computation graph.
pub fn generate_project(
    graph: &Graph,
    output_dir: &Path,
    model_name: &str,
    embed_weights: bool,
) -> Result<(), ProjectError> {
    let config = graph
        .config
        .as_ref()
        .ok_or(ProjectError::Codegen(emit::CodegenError::MissingConfig))?;

    let src_dir = output_dir.join("src");
    fs::create_dir_all(&src_dir).map_err(ProjectError::Io)?;

    // Generate model.rs
    let model_code = emit::generate(graph).map_err(ProjectError::Codegen)?;
    fs::write(src_dir.join("model.rs"), &model_code).map_err(ProjectError::Io)?;

    // Generate Cargo.toml
    fs::write(
        output_dir.join("Cargo.toml"),
        generate_cargo_toml(model_name),
    )
    .map_err(ProjectError::Io)?;

    // Generate main.rs
    let main_code = if embed_weights {
        generate_main_embedded(config)
    } else {
        generate_main(config)
    };
    fs::write(src_dir.join("main.rs"), main_code).map_err(ProjectError::Io)?;

    Ok(())
}

fn generate_cargo_toml(model_name: &str) -> String {
    let sanitized = model_name
        .to_lowercase()
        .replace(|c: char| !c.is_alphanumeric() && c != '-', "-")
        .trim_matches('-')
        .to_string();

    format!(
        r#"[package]
name = "{sanitized}"
version = "0.1.0"
edition = "2021"
description = "AOT-compiled LLM inference binary generated by ForgeLLM"

[dependencies]
tokenizers = {{ version = "0.21", default-features = false, features = ["onig"] }}
rayon = "1.10"

[profile.release]
opt-level = 3
lto = "thin"
codegen-units = 1
"#
    )
}

fn generate_main(config: &ModelConfig) -> String {
    let hidden = config.hidden_size;
    let inter = config.intermediate_size;
    let vocab = config.vocab_size;
    let num_heads = config.num_attention_heads;
    let num_kv_heads = config.num_kv_heads;
    let head_dim = config.head_dim;
    let arch = config.architecture.to_string();
    let _arch_lower = arch.to_lowercase();
    let num_layers = config.num_layers;
    let qk_size = num_heads * head_dim;
    let kv_size = num_kv_heads * head_dim;

    format!(
        r##"//! AOT-compiled inference binary generated by ForgeLLM.
//! Model: {arch} ({num_layers} layers, hidden={hidden})

mod model;

use std::env;
use std::io::{{Read, Write}};

fn load_weights(path: &str) -> Vec<f32> {{
    let mut file = std::fs::File::open(path).expect("failed to open weights");
    let mut bytes = Vec::new();
    file.read_to_end(&mut bytes).expect("failed to read weights");
    bytes.chunks_exact(4).map(|c| f32::from_le_bytes([c[0], c[1], c[2], c[3]])).collect()
}}

fn argmax(logits: &[f32]) -> u32 {{
    logits.iter().enumerate().max_by(|(_, a), (_, b)| a.partial_cmp(b).unwrap()).map(|(i, _)| i as u32).unwrap_or(0)
}}

fn main() {{
    let args: Vec<String> = env::args().collect();
    if args.len() < 3 {{
        eprintln!("Usage: {{}} <weights.bin> <tokenizer.json> [prompt]", args[0]);
        eprintln!("AOT-compiled {arch} | {num_layers} layers | hidden={hidden}");
        std::process::exit(1);
    }}

    let weights_path = &args[1];
    let tokenizer_path = &args[2];
    let prompt = if args.len() > 3 {{ args[3..].join(" ") }} else {{ "Hello".into() }};

    eprintln!("Loading tokenizer...");
    let tokenizer = tokenizers::Tokenizer::from_file(tokenizer_path).expect("failed to load tokenizer");

    eprintln!("Loading weights...");
    let w = load_weights(weights_path);
    let mut off = 0usize;

    let embed = w[off..off + {vocab} * {hidden}].to_vec(); off += {vocab} * {hidden};
    let mut layers = Vec::new();
    for _ in 0..{num_layers} {{
        let an = w[off..off+{hidden}].to_vec(); off += {hidden};
        let qp = w[off..off+{qk_size}*{hidden}].to_vec(); off += {qk_size}*{hidden};
        let kp = w[off..off+{kv_size}*{hidden}].to_vec(); off += {kv_size}*{hidden};
        let vp = w[off..off+{kv_size}*{hidden}].to_vec(); off += {kv_size}*{hidden};
        let op = w[off..off+{hidden}*{qk_size}].to_vec(); off += {hidden}*{qk_size};
        let fn_ = w[off..off+{hidden}].to_vec(); off += {hidden};
        let gp = w[off..off+{inter}*{hidden}].to_vec(); off += {inter}*{hidden};
        let up = w[off..off+{inter}*{hidden}].to_vec(); off += {inter}*{hidden};
        let dp = w[off..off+{hidden}*{inter}].to_vec(); off += {hidden}*{inter};
        layers.push(model::LayerWeights {{ attn_norm: an, q_proj: qp, k_proj: kp, v_proj: vp, o_proj: op, ffn_norm: fn_, gate_proj: gp, up_proj: up, down_proj: dp }});
    }}
    let fnorm = w[off..off+{hidden}].to_vec(); off += {hidden};
    let lmh = w[off..off+{vocab}*{hidden}].to_vec();
    let weights = model::Weights {{ embed_tokens: embed, layers, final_norm: fnorm, lm_head: lmh }};
    let mut cache = model::KVCache::new();

    let enc = tokenizer.encode(prompt.as_str(), false).expect("encode failed");
    let tokens = enc.get_ids();
    eprintln!("Prompt: {{}} tokens | Generating...", tokens.len());

    // Prefill
    let t0 = std::time::Instant::now();
    let mut next = 0u32;
    for &tok in tokens {{ let l = model::forward(tok, &weights, &mut cache); next = argmax(&l); }}
    eprintln!("Prefill: {{:.1}} tok/s", tokens.len() as f64 / t0.elapsed().as_secs_f64());

    // Generate
    print!("{{}}", prompt);
    let t1 = std::time::Instant::now();
    for _ in 0..128 {{
        let text = tokenizer.decode(&[next], true).unwrap_or_default();
        print!("{{}}", text);
        std::io::stdout().flush().ok();
        let l = model::forward(next, &weights, &mut cache);
        next = argmax(&l);
    }}
    println!();
    eprintln!("Generate: 128 tokens in {{:.2}}s ({{:.1}} tok/s)", t1.elapsed().as_secs_f64(), 128.0 / t1.elapsed().as_secs_f64());
}}
"##
    )
}

