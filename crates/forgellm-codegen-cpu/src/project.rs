//! Generate a complete Cargo project from a computation graph.
//!
//! Creates a directory structure with:
//! - Cargo.toml
//! - src/model.rs (kernels + forward function)
//! - src/main.rs (weight loader + CLI)

use std::fmt::Write;
use std::fs;
use std::path::Path;

use forgellm_frontend::ir::*;

use crate::emit;

/// Generate a complete Cargo project from a computation graph.
///
/// Creates a directory at `output_dir` with a buildable Rust project.
/// The generated binary loads weights from a GGUF file and runs inference.
pub fn generate_project(
    graph: &Graph,
    output_dir: &Path,
    model_name: &str,
) -> Result<(), ProjectError> {
    let config = graph
        .config
        .as_ref()
        .ok_or(ProjectError::Codegen(emit::CodegenError::MissingConfig))?;

    // Create directory structure
    let src_dir = output_dir.join("src");
    fs::create_dir_all(&src_dir).map_err(ProjectError::Io)?;

    // Generate model.rs (kernels + forward)
    let model_code = emit::generate(graph).map_err(ProjectError::Codegen)?;
    fs::write(src_dir.join("model.rs"), &model_code).map_err(ProjectError::Io)?;

    // Generate Cargo.toml
    let cargo_toml = generate_cargo_toml(model_name);
    fs::write(output_dir.join("Cargo.toml"), cargo_toml).map_err(ProjectError::Io)?;

    // Generate main.rs
    let main_rs = generate_main(config)?;
    fs::write(src_dir.join("main.rs"), main_rs).map_err(ProjectError::Io)?;

    Ok(())
}

fn generate_cargo_toml(model_name: &str) -> String {
    let sanitized = model_name
        .to_lowercase()
        .replace(|c: char| !c.is_alphanumeric() && c != '-', "-")
        .trim_matches('-')
        .to_string();

    format!(
        r#"[package]
name = "{sanitized}"
version = "0.1.0"
edition = "2021"
description = "AOT-compiled LLM inference binary generated by ForgeLLM"

[dependencies]
# No runtime dependencies — this is a self-contained binary

[profile.release]
opt-level = 3
lto = "thin"
codegen-units = 1
"#
    )
}

fn generate_main(config: &ModelConfig) -> Result<String, ProjectError> {
    let mut code = String::with_capacity(8 * 1024);

    writeln!(
        code,
        "//! AOT-compiled inference binary generated by ForgeLLM."
    )?;
    writeln!(
        code,
        "//! Model: {} ({} layers, hidden={})",
        config.architecture, config.num_layers, config.hidden_size
    )?;
    writeln!(code, "//!")?;
    writeln!(
        code,
        "//! Usage: ./{} <model.gguf> [prompt]",
        config.architecture.to_string().to_lowercase()
    )?;
    writeln!(code)?;
    writeln!(code, "mod model;")?;
    writeln!(code)?;
    writeln!(code, "use std::env;")?;
    writeln!(code, "use std::fs::File;")?;
    writeln!(code, "use std::io::Read;")?;
    writeln!(code)?;

    // Weight loader (minimal — reads raw f32 from a binary weights file)
    writeln!(code, "/// Load weights from a raw f32 binary file.")?;
    writeln!(
        code,
        "/// File format: concatenated f32 tensors in model order."
    )?;
    writeln!(code, "fn load_weights(path: &str) -> Vec<f32> {{")?;
    writeln!(
        code,
        "    let mut file = File::open(path).expect(\"failed to open weights file\");"
    )?;
    writeln!(code, "    let mut bytes = Vec::new();")?;
    writeln!(
        code,
        "    file.read_to_end(&mut bytes).expect(\"failed to read weights\");"
    )?;
    writeln!(code, "    bytes.chunks_exact(4)")?;
    writeln!(
        code,
        "        .map(|c| f32::from_le_bytes([c[0], c[1], c[2], c[3]]))"
    )?;
    writeln!(code, "        .collect()")?;
    writeln!(code, "}}")?;
    writeln!(code)?;

    // Weight splitting function
    let hidden = config.hidden_size;
    let inter = config.intermediate_size;
    let vocab = config.vocab_size;
    let num_heads = config.num_attention_heads;
    let num_kv_heads = config.num_kv_heads;
    let head_dim = config.head_dim;
    let _num_layers = config.num_layers;

    writeln!(code, "fn main() {{")?;
    writeln!(code, "    let args: Vec<String> = env::args().collect();")?;
    writeln!(code, "    if args.len() < 2 {{")?;
    writeln!(
        code,
        "        eprintln!(\"Usage: {{}} <weights.bin> [prompt]\", args[0]);"
    )?;
    writeln!(
        code,
        "        eprintln!(\"\\nThis is an AOT-compiled model binary.\");"
    )?;
    writeln!(code, "        eprintln!(\"Generate weights.bin with: forge export-weights <model.gguf> weights.bin\");")?;
    writeln!(code, "        std::process::exit(1);")?;
    writeln!(code, "    }}")?;
    writeln!(code)?;
    writeln!(code, "    let weights_path = &args[1];")?;
    writeln!(
        code,
        "    let prompt_text = if args.len() > 2 {{ &args[2] }} else {{ \"Hello\" }};"
    )?;
    writeln!(code)?;
    writeln!(
        code,
        "    eprintln!(\"Loading weights from {{weights_path}}...\");"
    )?;
    writeln!(code, "    let all_weights = load_weights(weights_path);")?;
    writeln!(code)?;

    // Split weights into the correct structs
    writeln!(code, "    // Split flat weight array into model structures")?;
    writeln!(code, "    let mut offset = 0usize;")?;
    writeln!(code, "    let embed_tokens: Vec<f32> = all_weights[offset..offset + {vocab} * {hidden}].to_vec();")?;
    writeln!(code, "    offset += {vocab} * {hidden};")?;
    writeln!(code)?;

    writeln!(code, "    let mut layers = Vec::new();")?;
    writeln!(code, "    for _ in 0..model::NUM_LAYERS {{")?;
    writeln!(code, "        let attn_norm = all_weights[offset..offset + {hidden}].to_vec(); offset += {hidden};")?;
    writeln!(code, "        let q_proj = all_weights[offset..offset + {} * {hidden}].to_vec(); offset += {} * {hidden};", num_heads * head_dim, num_heads * head_dim)?;
    writeln!(code, "        let k_proj = all_weights[offset..offset + {} * {hidden}].to_vec(); offset += {} * {hidden};", num_kv_heads * head_dim, num_kv_heads * head_dim)?;
    writeln!(code, "        let v_proj = all_weights[offset..offset + {} * {hidden}].to_vec(); offset += {} * {hidden};", num_kv_heads * head_dim, num_kv_heads * head_dim)?;
    writeln!(code, "        let o_proj = all_weights[offset..offset + {hidden} * {}].to_vec(); offset += {hidden} * {};", num_heads * head_dim, num_heads * head_dim)?;
    writeln!(code, "        let ffn_norm = all_weights[offset..offset + {hidden}].to_vec(); offset += {hidden};")?;
    writeln!(code, "        let gate_proj = all_weights[offset..offset + {inter} * {hidden}].to_vec(); offset += {inter} * {hidden};")?;
    writeln!(code, "        let up_proj = all_weights[offset..offset + {inter} * {hidden}].to_vec(); offset += {inter} * {hidden};")?;
    writeln!(code, "        let down_proj = all_weights[offset..offset + {hidden} * {inter}].to_vec(); offset += {hidden} * {inter};")?;
    writeln!(code, "        layers.push(model::LayerWeights {{ attn_norm, q_proj, k_proj, v_proj, o_proj, ffn_norm, gate_proj, up_proj, down_proj }});")?;
    writeln!(code, "    }}")?;
    writeln!(code)?;

    writeln!(
        code,
        "    let final_norm = all_weights[offset..offset + {hidden}].to_vec(); offset += {hidden};"
    )?;
    writeln!(
        code,
        "    let lm_head = all_weights[offset..offset + {vocab} * {hidden}].to_vec();"
    )?;
    writeln!(code)?;
    writeln!(
        code,
        "    let weights = model::Weights {{ embed_tokens, layers, final_norm, lm_head }};"
    )?;
    writeln!(code, "    let mut cache = model::KVCache::new();")?;
    writeln!(code)?;

    // Simple token-by-token generation (no tokenizer in AOT binary)
    writeln!(
        code,
        "    // Generate tokens (raw token IDs — no tokenizer in AOT binary)"
    )?;
    writeln!(
        code,
        "    // For full text generation, pair with a tokenizer"
    )?;
    writeln!(code, "    let token_id: u32 = 1; // BOS token")?;
    writeln!(code, "    eprintln!(\"Running forward pass...\");")?;
    writeln!(code)?;
    writeln!(code, "    let start = std::time::Instant::now();")?;
    writeln!(
        code,
        "    let logits = model::forward(token_id, &weights, &mut cache);"
    )?;
    writeln!(code, "    let elapsed = start.elapsed();")?;
    writeln!(code)?;
    writeln!(code, "    // Find top-5 tokens")?;
    writeln!(
        code,
        "    let mut indexed: Vec<(usize, f32)> = logits.iter().copied().enumerate().collect();"
    )?;
    writeln!(
        code,
        "    indexed.sort_by(|a, b| b.1.partial_cmp(&a.1).unwrap());"
    )?;
    writeln!(code)?;
    writeln!(code, "    println!(\"Top 5 predicted tokens:\");")?;
    writeln!(code, "    for (id, score) in indexed.iter().take(5) {{")?;
    writeln!(code, "        println!(\"  token {{id}}: {{score:.4}}\");")?;
    writeln!(code, "    }}")?;
    writeln!(
        code,
        "    eprintln!(\"Forward pass: {{:.2}}ms\", elapsed.as_secs_f64() * 1000.0);"
    )?;
    writeln!(
        code,
        "    eprintln!(\"Model: {} | {} layers | hidden={}\");",
        config.architecture, config.num_layers, config.hidden_size
    )?;
    writeln!(code, "}}")?;

    Ok(code)
}

/// Errors during project generation.
#[derive(Debug, thiserror::Error)]
pub enum ProjectError {
    #[error("I/O error: {0}")]
    Io(#[from] std::io::Error),

    #[error("codegen error: {0}")]
    Codegen(#[from] emit::CodegenError),

    #[error("format error: {0}")]
    Fmt(#[from] std::fmt::Error),
}
