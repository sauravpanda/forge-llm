fn generate_main_rs(model_name: &str, config: &ModelConfig) -> Result<String, GpuCodegenError> {
    let sanitized = sanitize_name(model_name);
    let vocab = config.vocab_size;
    let mut code = String::with_capacity(4 * 1024);
    writeln!(
        code,
        "//! Auto-generated GPU inference binary for `{model_name}`."
    )?;
    writeln!(code, "//!")?;
    writeln!(
        code,
        "//! Usage:  {sanitized} <weights.bin> <tokenizer.json> [prompt]"
    )?;
    writeln!(code)?;
    writeln!(code, "mod model;")?;
    writeln!(code, "use model::GpuModel;")?;
    writeln!(code)?;
    writeln!(code, "fn main() {{")?;
    writeln!(
        code,
        "    let args: Vec<String> = std::env::args().collect();"
    )?;
    writeln!(code, "    if args.len() < 3 {{")?;
    writeln!(
        code,
        "        eprintln!(\"Usage: {{}} <weights.bin> <tokenizer.json> [prompt]\", args[0]);"
    )?;
    writeln!(code, "        std::process::exit(1);")?;
    writeln!(code, "    }}")?;
    writeln!(code)?;
    writeln!(
        code,
        "    let weights = std::fs::read(&args[1]).expect(\"failed to read weights\");"
    )?;
    writeln!(code, "    let tokenizer_path = &args[2];")?;
    writeln!(
        code,
        "    let prompt = if args.len() > 3 {{ args[3].clone() }} else {{ \"Hello, world!\".to_string() }};"
    )?;
    writeln!(code)?;
    writeln!(code, "    println!(\"Initialising GPU model...\");")?;
    writeln!(code, "    let mut model = GpuModel::new(&weights);")?;
    writeln!(code)?;
    writeln!(code, "    // Load tokenizer and encode prompt")?;
    writeln!(
        code,
        "    let tokenizer = tokenizers::Tokenizer::from_file(tokenizer_path)"
    )?;
    writeln!(code, "        .expect(\"failed to load tokenizer\");")?;
    writeln!(
        code,
        "    let encoding = tokenizer.encode(prompt.as_str(), false).expect(\"tokenizer encode failed\");"
    )?;
    writeln!(
        code,
        "    let token_ids: Vec<u32> = encoding.get_ids().to_vec();"
    )?;
    writeln!(code)?;
    writeln!(code, "    println!(\"Prompt: {{prompt}}\");")?;
    writeln!(code, "    println!(\"Tokens: {{:?}}\", token_ids);")?;
    writeln!(code)?;
    writeln!(code, "    // Run forward pass for each token")?;
    writeln!(code, "    let mut last_logits = vec![0.0f32; {vocab}];")?;
    writeln!(
        code,
        "    for (pos, &token_id) in token_ids.iter().enumerate() {{"
    )?;
    writeln!(
        code,
        "        last_logits = model.forward(token_id, pos as u32);"
    )?;
    writeln!(code, "    }}")?;
    writeln!(code)?;
    writeln!(code, "    // Greedy decode: pick argmax of logits")?;
    writeln!(code, "    let next_token = last_logits")?;
    writeln!(code, "        .iter()")?;
    writeln!(code, "        .enumerate()")?;
    writeln!(
        code,
        "        .max_by(|(_, a), (_, b)| a.partial_cmp(b).unwrap())"
    )?;
    writeln!(code, "        .map(|(i, _)| i as u32)")?;
    writeln!(code, "        .unwrap_or(0);")?;
    writeln!(code)?;
    writeln!(code, "    let decoded = tokenizer")?;
    writeln!(code, "        .decode(&[next_token], true)")?;
    writeln!(
        code,
        "        .unwrap_or_else(|_| \"[decode error]\".to_string());"
    )?;
    writeln!(
        code,
        "    println!(\"Next token: {{next_token}} → \\\"{{decoded}}\\\"\");"
    )?;
    writeln!(code, "}}")?;

    Ok(code)
}

