//! Forge GPU Code Generation — WGSL compute shader emission.
//!
//! Generates a complete Cargo project that runs GPU inference via wgpu
//! (Vulkan, Metal, DX12, WebGPU). The project embeds WGSL compute shaders
//! as string constants and uses wgpu to dispatch them at runtime.

use std::fmt::Write as FmtWrite;
use std::fs;
use std::path::Path;

use forgellm_frontend::ir::*;

/// Errors during GPU code generation.
#[derive(Debug, thiserror::Error)]
pub enum GpuCodegenError {
    #[error("graph has no model config")]
    MissingConfig,

    #[error("I/O error: {0}")]
    Io(#[from] std::io::Error),

    #[error("format error: {0}")]
    Fmt(#[from] std::fmt::Error),
}

/// Generate a complete GPU Cargo project from a computation graph.
///
/// Creates:
/// - `Cargo.toml` — with wgpu + pollster + tokenizers dependencies
/// - `src/main.rs` — CLI that reads weights + tokenizer, runs GPU inference
/// - `src/model.rs` — GPU model with embedded WGSL compute shaders
pub fn generate_gpu_project(
    graph: &Graph,
    output_dir: &Path,
    model_name: &str,
) -> Result<(), GpuCodegenError> {
    let config = graph
        .config
        .as_ref()
        .ok_or(GpuCodegenError::MissingConfig)?;

    let src_dir = output_dir.join("src");
    fs::create_dir_all(&src_dir)?;

    fs::write(
        output_dir.join("Cargo.toml"),
        generate_cargo_toml(model_name),
    )?;

    let model_rs = generate_model_rs(config)?;
    fs::write(src_dir.join("model.rs"), model_rs)?;

    let main_rs = generate_main_rs(model_name, config)?;
    fs::write(src_dir.join("main.rs"), main_rs)?;

    Ok(())
}

// ---------------------------------------------------------------------------
// Internal helpers
// ---------------------------------------------------------------------------

fn sanitize_name(name: &str) -> String {
    name.to_lowercase()
        .replace(|c: char| !c.is_alphanumeric() && c != '-', "-")
        .trim_matches('-')
        .to_string()
}

fn generate_cargo_toml(model_name: &str) -> String {
    let sanitized = sanitize_name(model_name);
    format!(
        r#"[package]
name = "{sanitized}"
version = "0.1.0"
edition = "2021"

[[bin]]
name = "{sanitized}"
path = "src/main.rs"

[dependencies]
wgpu = "22"
pollster = "0.3"
tokenizers = {{ version = "0.21", default-features = false, features = ["onig"] }}

[profile.release]
opt-level = 3
lto = "fat"
codegen-units = 1
"#
    )
}

